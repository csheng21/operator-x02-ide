export function createIconElement(iconSvg: string): HTMLElement {
  const iconWrapper = document.createElement('span');
  iconWrapper.className = 'context-menu-icon';
  iconWrapper.innerHTML = iconSvg;
  iconWrapper.style.cssText = `
    display: inline-flex;
    align-items: center;
    justify-content: center;
    width: 16px;
    height: 16px;
    margin-right: 10px;
    color: #cccccc;
    flex-shrink: 0;
  `;
  return iconWrapper;
}

