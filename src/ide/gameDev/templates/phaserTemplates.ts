export const PLATFORMER_GAME_SCENE = `// ============================================================================
// 🏃 PLATFORMER GAME SCENE
// ============================================================================
// Generated by Operator X02 Code IDE
// ============================================================================

export class GameScene extends Phaser.Scene {
  // Player
  private player!: Phaser.Physics.Arcade.Sprite;
  private cursors!: Phaser.Types.Input.Keyboard.CursorKeys;
  
  // World
  private platforms!: Phaser.Physics.Arcade.StaticGroup;
  private coins!: Phaser.Physics.Arcade.Group;
  private enemies!: Phaser.Physics.Arcade.Group;
  
  // State
  private score: number = 0;
  private lives: number = 3;
  private isInvincible: boolean = false;
  
  // UI
  private scoreText!: Phaser.GameObjects.Text;
  private livesText!: Phaser.GameObjects.Text;

  constructor() {
    super({ key: 'GameScene' });
  }

  create(): void {
    const { width, height } = this.cameras.main;

    // Background gradient
    const bg = this.add.graphics();
    bg.fillGradientStyle(0x87CEEB, 0x87CEEB, 0x98FB98, 0x98FB98);
    bg.fillRect(0, 0, width, height);

    // Create platforms
    this.createPlatforms();
    
    // Create player
    this.createPlayer();
    
    // Create collectibles
    this.createCoins();
    
    // Create enemies
    this.createEnemies();
    
    // Setup collisions
    this.setupCollisions();
    
    // Setup input
    this.cursors = this.input.keyboard!.createCursorKeys();
    
    // UI
    this.createUI();

    console.log('🎮 Platformer GameScene started!');
  }

  private createPlatforms(): void {
    const { width, height } = this.cameras.main;
    
    this.platforms = this.physics.add.staticGroup();
    
    // Ground
    const ground = this.add.rectangle(width / 2, height - 20, width, 40, 0x8B4513);
    this.platforms.add(ground);
    
    // Floating platforms
    const platformData = [
      { x: 150, y: height - 150, w: 200 },
      { x: 450, y: height - 250, w: 150 },
      { x: 700, y: height - 150, w: 200 },
      { x: 300, y: height - 400, w: 180 },
      { x: 600, y: height - 350, w: 150 }
    ];
    
    platformData.forEach(p => {
      const platform = this.add.rectangle(p.x, p.y, p.w, 20, 0x654321);
      this.platforms.add(platform);
    });
  }

  private createPlayer(): void {
    const { height } = this.cameras.main;
    
    // Create player graphics
    const graphics = this.add.graphics();
    graphics.fillStyle(0x00ff88);
    graphics.fillRect(0, 0, 32, 48);
    graphics.generateTexture('player', 32, 48);
    graphics.destroy();
    
    this.player = this.physics.add.sprite(100, height - 100, 'player');
    this.player.setCollideWorldBounds(true);
    this.player.setBounce(0.1);
  }

  private createCoins(): void {
    this.coins = this.physics.add.group();
    
    const coinPositions = [
      { x: 150, y: 200 }, { x: 250, y: 200 },
      { x: 450, y: 150 }, { x: 550, y: 150 },
      { x: 700, y: 200 }, { x: 300, y: 300 },
      { x: 600, y: 250 }
    ];
    
    coinPositions.forEach(pos => {
      const coin = this.add.circle(pos.x, pos.y, 12, 0xFFD700);
      this.coins.add(coin);
      (coin.body as Phaser.Physics.Arcade.Body).setAllowGravity(false);
      
      // Floating animation
      this.tweens.add({
        targets: coin,
        y: pos.y - 10,
        duration: 1000,
        yoyo: true,
        repeat: -1,
        ease: 'Sine.easeInOut'
      });
    });
  }

  private createEnemies(): void {
    this.enemies = this.physics.add.group();
    
    const enemyPositions = [
      { x: 300, y: 500, patrol: { min: 200, max: 400 } },
      { x: 600, y: 350, patrol: { min: 500, max: 700 } }
    ];
    
    enemyPositions.forEach(pos => {
      const enemy = this.add.circle(pos.x, pos.y, 20, 0xFF4444);
      this.enemies.add(enemy);
      (enemy.body as Phaser.Physics.Arcade.Body).setAllowGravity(false);
      
      // Patrol movement
      this.tweens.add({
        targets: enemy,
        x: pos.patrol.max,
        duration: 2000,
        yoyo: true,
        repeat: -1,
        ease: 'Linear'
      });
    });
  }

  private setupCollisions(): void {
    // Player collides with platforms
    this.physics.add.collider(this.player, this.platforms);
    
    // Collect coins
    this.physics.add.overlap(
      this.player, 
      this.coins, 
      this.collectCoin as any, 
      undefined, 
      this
    );
    
    // Enemy collision
    this.physics.add.overlap(
      this.player,
      this.enemies,
      this.hitEnemy as any,
      undefined,
      this
    );
  }

  private createUI(): void {
    this.scoreText = this.add.text(16, 16, 'Score: 0', {
      font: 'bold 24px Arial',
      color: '#000000'
    }).setScrollFactor(0);
    
    this.livesText = this.add.text(16, 50, '❤️❤️❤️', {
      font: '24px Arial'
    }).setScrollFactor(0);
  }

  private collectCoin(player: any, coin: any): void {
    coin.destroy();
    this.score += 10;
    this.scoreText.setText(\`Score: \${this.score}\`);
    
    // Check if all coins collected
    if (this.coins.countActive() === 0) {
      this.createCoins();
    }
  }

  private hitEnemy(player: any, enemy: any): void {
    if (this.isInvincible) return;
    
    // Check if stomping
    const playerBody = player.body as Phaser.Physics.Arcade.Body;
    const enemyBody = enemy.body as Phaser.Physics.Arcade.Body;
    
    if (playerBody.velocity.y > 0 && player.y < enemy.y - 10) {
      // Stomp enemy
      enemy.destroy();
      playerBody.setVelocityY(-300);
      this.score += 50;
      this.scoreText.setText(\`Score: \${this.score}\`);
    } else {
      // Take damage
      this.takeDamage();
    }
  }

  private takeDamage(): void {
    this.lives--;
    this.livesText.setText('❤️'.repeat(this.lives));
    
    if (this.lives <= 0) {
      this.scene.start('GameOverScene', { score: this.score });
      return;
    }
    
    // Invincibility frames
    this.isInvincible = true;
    this.player.setTint(0xff0000);
    
    this.time.delayedCall(1500, () => {
      this.isInvincible = false;
      this.player.clearTint();
    });
  }

  update(): void {
    const body = this.player.body as Phaser.Physics.Arcade.Body;
    
    // Horizontal movement
    if (this.cursors.left.isDown) {
      body.setVelocityX(-200);
      this.player.setFlipX(true);
    } else if (this.cursors.right.isDown) {
      body.setVelocityX(200);
      this.player.setFlipX(false);
    } else {
      body.setVelocityX(0);
    }
    
    // Jumping
    if (this.cursors.up.isDown && body.onFloor()) {
      body.setVelocityY(-450);
    }
    
    // Fall death
    if (this.player.y > this.cameras.main.height + 50) {
      this.takeDamage();
      this.player.setPosition(100, 100);
      body.setVelocity(0);
    }
  }
}
`;

