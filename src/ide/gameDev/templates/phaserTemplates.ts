export const TOPDOWN_GAME_SCENE = `// ============================================================================
// 🗺️ TOP-DOWN ADVENTURE GAME SCENE
// ============================================================================
// Generated by Operator X02 Code IDE
// ============================================================================

export class GameScene extends Phaser.Scene {
  // Player
  private player!: Phaser.Physics.Arcade.Sprite;
  private cursors!: Phaser.Types.Input.Keyboard.CursorKeys;
  
  // World
  private walls!: Phaser.Physics.Arcade.StaticGroup;
  private coins!: Phaser.Physics.Arcade.Group;
  private npcs!: Phaser.Physics.Arcade.Group;
  
  // State
  private score: number = 0;
  private isInDialogue: boolean = false;
  
  // UI
  private scoreText!: Phaser.GameObjects.Text;
  private dialogueBox!: Phaser.GameObjects.Container;

  constructor() {
    super({ key: 'GameScene' });
  }

  create(): void {
    const { width, height } = this.cameras.main;

    // Grass background
    this.add.rectangle(0, 0, width, height, 0x3d8b40).setOrigin(0);
    
    // Create world
    this.createWalls();
    this.createPlayer();
    this.createCoins();
    this.createNPCs();
    
    // Collisions
    this.physics.add.collider(this.player, this.walls);
    this.physics.add.overlap(this.player, this.coins, this.collectCoin as any, undefined, this);
    this.physics.add.overlap(this.player, this.npcs, this.interactNPC as any, undefined, this);
    
    // Input
    this.cursors = this.input.keyboard!.createCursorKeys();
    this.input.keyboard!.on('keydown-E', () => this.tryInteract());
    
    // UI
    this.createUI();

    console.log('🎮 Top-Down GameScene started!');
  }

  private createWalls(): void {
    const { width, height } = this.cameras.main;
    
    this.walls = this.physics.add.staticGroup();
    
    // Border walls
    this.addWall(width / 2, 15, width, 30);
    this.addWall(width / 2, height - 15, width, 30);
    this.addWall(15, height / 2, 30, height);
    this.addWall(width - 15, height / 2, 30, height);
    
    // Interior walls (house/building)
    this.addWall(200, 150, 150, 100);
    this.addWall(500, 300, 100, 150);
    this.addWall(350, 450, 200, 30);
  }

  private addWall(x: number, y: number, w: number, h: number): void {
    const wall = this.add.rectangle(x, y, w, h, 0x5c4033);
    this.walls.add(wall);
  }

  private createPlayer(): void {
    const { height } = this.cameras.main;
    
    // Create player texture
    const graphics = this.add.graphics();
    graphics.fillStyle(0x00ff88);
    graphics.fillRect(0, 0, 28, 28);
    graphics.fillStyle(0x008844);
    graphics.fillRect(8, 4, 12, 8); // Face area
    graphics.generateTexture('player', 28, 28);
    graphics.destroy();
    
    this.player = this.physics.add.sprite(100, height - 100, 'player');
    this.player.setCollideWorldBounds(true);
  }

  private createCoins(): void {
    this.coins = this.physics.add.group();
    
    const positions = [
      { x: 150, y: 300 }, { x: 400, y: 150 },
      { x: 600, y: 200 }, { x: 200, y: 500 },
      { x: 650, y: 450 }, { x: 400, y: 400 }
    ];
    
    positions.forEach(pos => {
      const coin = this.add.circle(pos.x, pos.y, 10, 0xffd700);
      this.coins.add(coin);
    });
  }

  private createNPCs(): void {
    this.npcs = this.physics.add.group();
    
    const npcData = [
      { x: 350, y: 200, color: 0x4dabf7, dialogue: 'Hello adventurer! Collect all the coins!' },
      { x: 550, y: 500, color: 0xff6b6b, dialogue: 'Watch out for the walls!' }
    ];
    
    npcData.forEach(data => {
      const npc = this.add.rectangle(data.x, data.y, 30, 30, data.color);
      npc.setData('dialogue', data.dialogue);
      this.npcs.add(npc);
      (npc.body as Phaser.Physics.Arcade.Body).setImmovable(true);
    });
  }

  private createUI(): void {
    this.scoreText = this.add.text(16, 16, 'Coins: 0', {
      font: 'bold 24px Arial',
      color: '#ffffff',
      backgroundColor: '#00000088',
      padding: { x: 10, y: 5 }
    }).setScrollFactor(0);
    
    // Dialogue box (hidden initially)
    this.dialogueBox = this.add.container(400, 500);
    const bg = this.add.rectangle(0, 0, 600, 100, 0x000000, 0.8);
    const text = this.add.text(0, 0, '', {
      font: '18px Arial',
      color: '#ffffff',
      wordWrap: { width: 560 }
    }).setOrigin(0.5);
    const hint = this.add.text(270, 35, '[Press E to close]', {
      font: '12px Arial',
      color: '#888888'
    }).setOrigin(1, 0.5);
    
    this.dialogueBox.add([bg, text, hint]);
    this.dialogueBox.setVisible(false);
    this.dialogueBox.setScrollFactor(0);
  }

  private collectCoin(player: any, coin: any): void {
    coin.destroy();
    this.score++;
    this.scoreText.setText(\`Coins: \${this.score}\`);
    
    if (this.coins.countActive() === 0) {
      this.createCoins();
    }
  }

  private interactNPC(player: any, npc: any): void {
    // Just register that we're near an NPC
    npc.setData('nearby', true);
  }

  private tryInteract(): void {
    if (this.isInDialogue) {
      this.closeDialogue();
      return;
    }
    
    // Find nearby NPC
    this.npcs.getChildren().forEach((npc: any) => {
      const dist = Phaser.Math.Distance.Between(
        this.player.x, this.player.y,
        npc.x, npc.y
      );
      
      if (dist < 50) {
        this.showDialogue(npc.getData('dialogue'));
      }
    });
  }

  private showDialogue(text: string): void {
    this.isInDialogue = true;
    const textObj = this.dialogueBox.getAt(1) as Phaser.GameObjects.Text;
    textObj.setText(text);
    this.dialogueBox.setVisible(true);
  }

  private closeDialogue(): void {
    this.isInDialogue = false;
    this.dialogueBox.setVisible(false);
  }

  update(): void {
    if (this.isInDialogue) return;
    
    const body = this.player.body as Phaser.Physics.Arcade.Body;
    const speed = 160;
    
    body.setVelocity(0);
    
    if (this.cursors.left.isDown) {
      body.setVelocityX(-speed);
    } else if (this.cursors.right.isDown) {
      body.setVelocityX(speed);
    }
    
    if (this.cursors.up.isDown) {
      body.setVelocityY(-speed);
    } else if (this.cursors.down.isDown) {
      body.setVelocityY(speed);
    }
    
    // Normalize diagonal movement
    if (body.velocity.x !== 0 && body.velocity.y !== 0) {
      body.velocity.normalize().scale(speed);
    }
  }
}
`;

