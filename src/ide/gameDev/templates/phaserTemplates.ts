export const SHOOTER_GAME_SCENE = `// ============================================================================
// 🚀 SPACE SHOOTER GAME SCENE
// ============================================================================
// Generated by Operator X02 Code IDE
// ============================================================================

export class GameScene extends Phaser.Scene {
  // Player
  private player!: Phaser.GameObjects.Triangle;
  
  // Groups
  private bullets!: Phaser.GameObjects.Group;
  private enemies!: Phaser.GameObjects.Group;
  private powerups!: Phaser.GameObjects.Group;
  private particles!: Phaser.GameObjects.Group;
  
  // Input
  private cursors!: Phaser.Types.Input.Keyboard.CursorKeys;
  private spaceKey!: Phaser.Input.Keyboard.Key;
  
  // State
  private score: number = 0;
  private lives: number = 3;
  private level: number = 1;
  private lastShot: number = 0;
  private lastSpawn: number = 0;
  private rapidFire: boolean = false;
  private shield: boolean = false;
  
  // UI
  private scoreText!: Phaser.GameObjects.Text;
  private livesText!: Phaser.GameObjects.Text;
  private levelText!: Phaser.GameObjects.Text;

  constructor() {
    super({ key: 'GameScene' });
  }

  create(): void {
    const { width, height } = this.cameras.main;

    // Starfield background
    this.createStarfield();
    
    // Create player
    this.createPlayer();
    
    // Initialize groups
    this.bullets = this.add.group();
    this.enemies = this.add.group();
    this.powerups = this.add.group();
    this.particles = this.add.group();
    
    // Input
    this.cursors = this.input.keyboard!.createCursorKeys();
    this.spaceKey = this.input.keyboard!.addKey(Phaser.Input.Keyboard.KeyCodes.SPACE);
    
    // UI
    this.createUI();

    console.log('🎮 Space Shooter GameScene started!');
  }

  private createStarfield(): void {
    const { width, height } = this.cameras.main;
    
    for (let i = 0; i < 100; i++) {
      const star = this.add.circle(
        Math.random() * width,
        Math.random() * height,
        Math.random() * 2 + 0.5,
        0xffffff,
        Math.random() * 0.5 + 0.3
      );
      
      this.tweens.add({
        targets: star,
        y: height + 10,
        duration: Math.random() * 3000 + 2000,
        repeat: -1,
        onRepeat: () => {
          star.y = -10;
          star.x = Math.random() * width;
        }
      });
    }
  }

  private createPlayer(): void {
    const { width, height } = this.cameras.main;
    
    this.player = this.add.triangle(
      width / 2, height - 60,
      0, 30, 20, 0, 40, 30,
      0x00ff88
    );
    
    this.physics.add.existing(this.player);
    (this.player.body as Phaser.Physics.Arcade.Body).setCollideWorldBounds(true);
  }

  private createUI(): void {
    const { width } = this.cameras.main;
    
    this.scoreText = this.add.text(16, 16, 'Score: 0', {
      font: 'bold 24px Arial',
      color: '#00ff88'
    });
    
    this.levelText = this.add.text(width / 2, 16, 'Level: 1', {
      font: 'bold 20px Arial',
      color: '#4dabf7'
    }).setOrigin(0.5, 0);
    
    this.livesText = this.add.text(width - 16, 16, '❤️❤️❤️', {
      font: '24px Arial'
    }).setOrigin(1, 0);
  }

  private shoot(): void {
    const now = this.time.now;
    const cooldown = this.rapidFire ? 100 : 200;
    
    if (now - this.lastShot < cooldown) return;
    this.lastShot = now;
    
    const bullet = this.add.rectangle(
      this.player.x, this.player.y - 20,
      4, 15, 0x00ffff
    );
    this.bullets.add(bullet);
  }

  private spawnEnemy(): void {
    const { width } = this.cameras.main;
    const x = Math.random() * (width - 60) + 30;
    
    // Different enemy types based on level
    const isFast = Math.random() < 0.2 + this.level * 0.05;
    const isShooter = Math.random() < 0.1 + this.level * 0.03;
    
    const enemy = this.add.circle(x, -30, isFast ? 15 : 20, isShooter ? 0xff0000 : 0xff6600);
    enemy.setData('type', isShooter ? 'shooter' : (isFast ? 'fast' : 'basic'));
    enemy.setData('speed', isFast ? 4 : 2 + this.level * 0.3);
    
    this.enemies.add(enemy);
  }

  private spawnPowerup(x: number, y: number): void {
    if (Math.random() > 0.2) return; // 20% chance
    
    const types = ['rapid', 'shield', 'life'];
    const type = Phaser.Math.RND.pick(types);
    const colors: Record<string, number> = {
      rapid: 0xff8800,
      shield: 0x00aaff,
      life: 0xff0000
    };
    
    const powerup = this.add.circle(x, y, 12, colors[type]);
    powerup.setData('type', type);
    this.powerups.add(powerup);
  }

  private createExplosion(x: number, y: number, color: number = 0xff8800): void {
    for (let i = 0; i < 10; i++) {
      const particle = this.add.circle(x, y, Math.random() * 4 + 2, color);
      this.particles.add(particle);
      
      const angle = Math.random() * Math.PI * 2;
      const speed = Math.random() * 100 + 50;
      
      this.tweens.add({
        targets: particle,
        x: x + Math.cos(angle) * speed,
        y: y + Math.sin(angle) * speed,
        alpha: 0,
        scale: 0,
        duration: 500,
        onComplete: () => particle.destroy()
      });
    }
  }

  private checkCollisions(): void {
    // Bullets vs Enemies
    this.bullets.getChildren().forEach((bullet: any) => {
      this.enemies.getChildren().forEach((enemy: any) => {
        if (Phaser.Geom.Intersects.RectangleToRectangle(
          bullet.getBounds(),
          enemy.getBounds()
        )) {
          this.createExplosion(enemy.x, enemy.y);
          this.spawnPowerup(enemy.x, enemy.y);
          
          bullet.destroy();
          enemy.destroy();
          
          this.score += enemy.getData('type') === 'shooter' ? 150 : 100;
          this.scoreText.setText(\`Score: \${this.score}\`);
          
          // Level up
          if (this.score >= this.level * 1000) {
            this.level++;
            this.levelText.setText(\`Level: \${this.level}\`);
          }
        }
      });
    });
    
    // Enemies vs Player
    if (!this.shield) {
      this.enemies.getChildren().forEach((enemy: any) => {
        if (Phaser.Geom.Intersects.RectangleToRectangle(
          this.player.getBounds(),
          enemy.getBounds()
        )) {
          this.createExplosion(enemy.x, enemy.y);
          enemy.destroy();
          this.loseLife();
        }
      });
    }
    
    // Powerups vs Player
    this.powerups.getChildren().forEach((powerup: any) => {
      if (Phaser.Geom.Intersects.RectangleToRectangle(
        this.player.getBounds(),
        powerup.getBounds()
      )) {
        const type = powerup.getData('type');
        powerup.destroy();
        
        switch (type) {
          case 'rapid':
            this.rapidFire = true;
            this.time.delayedCall(5000, () => { this.rapidFire = false; });
            break;
          case 'shield':
            this.shield = true;
            this.player.setFillStyle(0x00aaff);
            this.time.delayedCall(5000, () => {
              this.shield = false;
              this.player.setFillStyle(0x00ff88);
            });
            break;
          case 'life':
            this.lives = Math.min(this.lives + 1, 5);
            this.livesText.setText('❤️'.repeat(this.lives));
            break;
        }
      }
    });
  }

  private loseLife(): void {
    this.lives--;
    this.livesText.setText('❤️'.repeat(this.lives));
    
    if (this.lives <= 0) {
      this.scene.start('GameOverScene', { score: this.score });
    }
  }

  update(time: number): void {
    const body = this.player.body as Phaser.Physics.Arcade.Body;
    const { width, height } = this.cameras.main;
    
    // Player movement
    if (this.cursors.left.isDown) {
      body.setVelocityX(-300);
    } else if (this.cursors.right.isDown) {
      body.setVelocityX(300);
    } else {
      body.setVelocityX(0);
    }
    
    // Shooting
    if (this.spaceKey.isDown) {
      this.shoot();
    }
    
    // Spawn enemies
    if (time - this.lastSpawn > 1000 - this.level * 50) {
      this.lastSpawn = time;
      this.spawnEnemy();
    }
    
    // Update bullets
    this.bullets.getChildren().forEach((bullet: any) => {
      bullet.y -= 10;
      if (bullet.y < -20) bullet.destroy();
    });
    
    // Update enemies
    this.enemies.getChildren().forEach((enemy: any) => {
      const speed = enemy.getData('speed') || 2;
      enemy.y += speed;
      if (enemy.y > height + 30) enemy.destroy();
    });
    
    // Update powerups
    this.powerups.getChildren().forEach((powerup: any) => {
      powerup.y += 2;
      if (powerup.y > height + 20) powerup.destroy();
    });
    
    // Check collisions
    this.checkCollisions();
  }
}
`;

